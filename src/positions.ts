export function watchLocation() {
    return from(positions).pipe(
        // Emit each object and introduce a delay based on the timestamp difference
        concatMap((obj, index) => {
            // Calculate the delay for each item
            const timeToDelay =
                index === 0
                    ? 0
                    : obj.timestamp - positions[index - 1].timestamp;
            // Delay the emission of the current object
            return from([obj]).pipe(delay(timeToDelay));
        })
    );
}
